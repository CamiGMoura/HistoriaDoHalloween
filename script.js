function menu(){
    document.getElementById("abrir").style.width ="25%";
    document.getElementById("conteudo").style.marginLeft ="25%"
    document.getElementById("alunos").style.marginLeft ="25%"
    document.getElementById("menu").style.display = "none";
}
function fechar(){
    document.getElementById("abrir").style.width ="0px"
    document.getElementById("conteudo").style.marginLeft ="0px"
    document.getElementById("alunos").style.marginLeft ="0px"
    document.getElementById("menu").style.display = "inline";
}